package com.material.materialdesign2.Models;

public class Plan {
    private int id;
    private String title;
    private String dateJSONArray;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public String getDateJSONArray() {
        return dateJSONArray;
    }

    public void setDateJSONArray(String dateJSONArray) {
        this.dateJSONArray = dateJSONArray;
    }
}
